'''Create a Python program that will have one tuple of vegetables with values = (‘Potato’,
‘Brinjal’,‘Tomato’,‘Cabbage’, ‘Cauliflower’). Perform following operations:
• Print whole tuple
• Print only first element of tuple0
• Prints elements starting from 2nd till 4th
• Prints elements starting from 2nd element till last
• Print whole tuple twice using appropriate operator.'''

vegetables = ('Potato', 'Brinjal', 'Tomato', 'Cabbage', 'Cauliflower')

# Print whole tuple
print(vegetables)

# Print only first element of tuple
print(vegetables[0])

# Print elements starting from 2nd till 4th
print(vegetables[1:4])

# Print elements starting from 2nd element till last
print(vegetables[1:])

# Print whole tuple twice using appropriate operator
print(vegetables * 2)
