"""Create a Python program that will have 3 variables which stores integer, float and
complex value. Display its value and also demonstrate its datatype class using type()."""

# Integer value
int_value = 10
print("Integer value:", int_value, "Type:", type(int_value))

# Float value
float_value = 10.5
print("Float value:", float_value, "Type:", type(float_value))

# Complex value
complex_value = 3 + 4j
print("Complex value:", complex_value, "Type:", type(complex_value))
