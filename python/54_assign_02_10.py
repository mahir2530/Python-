dict = {
    "Rohan" : 1,
    "Adi" : 2,
    "Ishan" : 3,
    "Yash" : 4,
    "Jay" : 5
}

keys = dict.keys()
values = dict.values()
items = dict.items()

print("Keys  : ")
for key in keys:
    print(key)

print("\nValues  : ")
for value in values:
    print(value)

print("\nItems  : ")
for item in items:
    print(item)